/*
一、定义类（必背）
1?? 广义表定义（标准表述）

广义表是 n（n ≥ 0）个元素组成的有限序列
其中每个元素：
要么是原子（atom）
要么是一个广义表

关键词：
有限
有序
元素可以是表本身

2?? 原子 vs 子表（必考判断）
原子：不可再分的基本数据
子表：广义表类型的元素
? 考点：
“空表不是原子”
“空表是一个合法广义表”

3?? 空表 ()（必考）
长度 = 0
深度 = 1（?? 很多教材定义为 1）

二、表示法（高频）
4?? 括号表示法（100% 会考）
例如：
A = (a, (b, c), (), d)

你要会：
判断哪些是原子
哪些是子表

5?? 头尾表示法（必考）
A = (a, b, c)
Head(A) = a
Tail(A) = (b, c)

注意：
Tail 永远是 表
(a) 的 Tail 是 ()

三、长度与深度（最爱考）
6?? 长度（Length）

长度 = 顶层元素个数
例：
(a, (b, c), d)
长度 = 3
(b, c) 算 1 个元素

7?? 深度（Depth）?? 必考坑点

深度 = 最大嵌套层数
规则（常用教材定义）：
原子深度 = 0
空表深度 = 1
非空表深度 = 1 + max(子表深度)
例：

A = (a, (b, (c)))

计算：
(c) 深度 = 1
(b, (c)) 深度 = 2
A 深度 = 3


四、递归性质（命题题）
8?? 递归定义（会让你补空）
若 A 是原子，则 A 是广义表；
若 A = (a1, a2, ..., an)，其中 ai 是原子或广义表，
则 A 是广义表。

9?? 递归算法模板（会让你写伪代码） ★
求深度（标准答案形式）
Depth(G):
  if G 是原子 return 0
  if G 是空表 return 1
  max = 0
  for 每个元素 e ∈ G:
      max = max(max, Depth(e))
  return max + 1

? 循环 + 递归混合结构是标准写法
*/