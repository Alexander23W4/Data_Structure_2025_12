/*

| 操作                | 功能                | AO 示例          | 复杂度                         |
| ----------------- | ----------------- | -------------- | --------------------------- |
| `find(x)`         | 查找 x 所属集合代表（根节点）  | 判断两个玩家是否在同一个联盟 | O(α(n)) ― α = 阿克曼函数，几乎 O(1) |
| `union(x, y)`     | 合并 x 所属集合和 y 所属集合 | 合并两个联盟或团队      | O(α(n))                     |
| `connected(x, y)` | 判断 x、y 是否同属一个集合   | 判断网络中两台服务器是否连通 | O(α(n))                     |

*/

/*
Application:

社交网络
用户加入群组
群组合并 → union
判断两用户是否已在同一群 → connected

网络连通性
服务器上线/下线 → union / disconnect
判断网络分区 → find
最小生成树

Kruskal 算法：
按边权排序
选择边 u-v：
如果 find(u) ≠ find(v) → union(u,v)
否则跳过（避免成环）

*/